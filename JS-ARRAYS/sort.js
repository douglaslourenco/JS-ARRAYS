
// É usado para ordenar os elementos do array em ordem alfabética ou numérica.

const nomes = ['Douglas', 'Esther', 'Deivisson', 'Maria', 'Jeovane', 'Fernanda']

nomes.sort();

console.log(nomes); // Saída: [ 'Deivisson', 'Douglas', 'Esther', 'Fernanda', 'Jeovane', 'Maria' ]

const numeros = [50, 30, 40, 10, 20];

numeros.sort();

console.log(numeros); // Saída: [ 10, 20, 30, 40, 50 ]

